    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        cliente = new Cliente();
        cliente.setNome(vNome.getText());
        cliente.setEmail(vEmail.getText());
        cliente.setTelefone(vTelefone.getText());
        cliente.setSalario(Double.parseDouble(vSalario.getText()));
        clienteDao = new ClienteDaoImpl();
        try {
            clienteDao.salvar(cliente);
            JOptionPane.showMessageDialog(null, "Cliente salvo com sucesso!");
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, "Erro ao salvar Cliente");
            System.out.println("erro ao salvar na tela o cliente " + e.getMessage());
        }
        limpar();
//        atualizarTabelaCliente();
    }//GEN-LAST:event_btSalvarActionPerformed

    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int linhaSelecionada = tabelaClientes.getSelectedRow();
        if (linhaSelecionada >= 0) {
            cliente = clientes.get(linhaSelecionada);            
            vNome.setText(cliente.getNome());
            vTelefone.setText(cliente.getTelefone());
            vEmail.setText(cliente.getEmail());
            vSalario.setText(cliente.getSalario() + "");
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btLimparActionPerformed
        limpar();
    }//GEN-LAST:event_btLimparActionPerformed

    private double converteSalario(String salarioTela) {
        String valor = salarioTela.replace(".", "");
        valor = valor.replace(",", ".");
        return Double.parseDouble(valor);
    }

    private void limpar() {
        vNome.setText(null);
        vTelefone.setText(null);
        vEmail.setText(null);
        vSalario.setText(null);
    }

