    private void atualizarTabelaCliente() {
        DefaultTableModel tabela = (DefaultTableModel) tabelaClientes.getModel();

        tabela.setNumRows(0);
        int tamanho = clientes.size();
        Cliente cliente;
        for (int linha = 0; linha < tamanho; linha++) {
            cliente = clientes.get(linha);
            tabela.addRow(new Object[]{1});
            tabelaClientes.setValueAt(cliente.getNome(), linha, 0);
            tabelaClientes.setValueAt(cliente.getEmail(), linha, 1);
            tabelaClientes.setValueAt(cliente.getTelefone(), linha, 2);
            tabelaClientes.setValueAt(cliente.getSalario(), linha, 3);
        }

    }

